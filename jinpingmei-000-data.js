    "《金瓶梅》，穢書也。袁石公亟稱之，亦自寄其牢騷耳，非有取於《金瓶梅》也。然作者亦自有意，蓋為世戒，非為世勸也。如諸婦多矣，而獨以潘金蓮、李瓶兒、春梅命名者，亦楚《檮杌》之意也。蓋金蓮以奸死，瓶兒以孽死，春梅以淫死，較諸婦為更慘耳。借西門慶以描畫世之大淨，應伯爵以描畫世之小丑，諸淫婦以描畫世之醜婆淨婆，令人讀之汗下。蓋為世戒，非為世勸也。餘嘗曰：讀《金瓶梅》而生憐憫心者，菩薩也；生畏懼心者，君子也；生歡喜心者，小人也；生傚法心者，乃禽獸耳。餘友人褚孝秀，偕一少年同赴歌舞之筵，衍至《霸王夜宴》，少年垂涎曰：「男兒何可不如此！」孝秀曰：「也只為這烏江設此一著耳。」同座聞之，歎為有道之言。若有人識得此意，方許他讀《金瓶梅》也。不然，石公幾為導淫宣欲之尤矣！奉勸世人，勿為西門慶之後車可也。",
