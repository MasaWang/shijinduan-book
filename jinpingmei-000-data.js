    "夢梅館校本《金瓶梅詞話》前言",
    "《金瓶梅詞話》是中國著名的古典長篇白話小説，也是最具爭論性的小説。自誕生以來，貶之者詆為「市諢之極穢者」（明李日華·味水軒日記），「當急投秦火」（明薛岡·天爵堂筆餘）；讚之者譽為「偉大的寫實小説」（鄭振鐸·談金瓶梅詞話），「同時説部，無以上之」（魯迅·中國小説史略）。其實，除去書中一些不雅的性描寫，《金瓶梅詞話》無疑是中國文學寶庫中之奇珍，與《水滸傳》、《紅樓夢》屬同一水平的作品。《金瓶梅》接枝自《水滸》，《紅樓夢》脫胎於《金瓶》；《水滸傳》寫江湖，《金瓶梅》寫市井，《紅樓夢》寫上層貴族，均曲盡其妙。《金瓶梅詞話》的文學地位，亦居於二者之間。",
    "《金瓶梅》的作者，在文學史上至今仍是個謎。欣欣子序雖有「蘭陵笑笑生作金瓶梅傳」的説法，真實姓名與生平事蹟均語焉不詳，且亦不見早期抄閱者著錄。後人指為王世貞、李開先、賈三近、屠隆等等，皆缺乏可靠證據。從本書的題旨、內容、取材、敘述結構和語言特徵看，應屬大眾消費性通俗文學，以平話為主體，穿插演唱流行曲。其作者是書會才人一類中下層知識分子，可能與流傳久遠的「羅公（貫中）書會」有某種關係。現存今本詞話，應為民間説書藝人的底本，有的學者甚至認為是「耳錄」（傅憎享·金瓶梅隱語揭秘）。",
    "《金瓶梅》借北宋年號名色，刻劃明代人情世態，開創中國古典長篇小説寫實的傳統。小説講述在明季商品經濟新潮中，一位破落戶市棍，欺壓良善，結交勢要，官商通吃，飛黃驣達，享受榮華富貴，最後縱欲身亡的故事。書中的清河，當是運河沿岸一個城鎮。據書中稱淮安、清江浦為「淮上」，稱揚州為「下州」，稱淮河為「南河」，生活場景接近南清河的淮揚地區。《金瓶梅詞話》最初大概就由打談的在淮安、揚州、臨清、濟寧等繁榮、富庶的運河大碼頭上説唱，後來也傳至運河南端的蘇州和杭州（明張岱·陶庵夢憶·不繫園）。聽眾多為客商、船伕、手藝人和市民。",
    "《金瓶梅詞話》借樹開花，從《水滸》武松打虎故事直接切入，開頭有五、六回文字摭自《水滸》，其成書上限不能早於現存百回本《忠義水滸傳》的定型和刊行。書中寫官哥、李瓶兒、西門慶之喪，用的十二個日、月干支，均隆慶五年八月到六年二月的干支；第六十八回提到的「南河南徙」，始於萬暦五年閠八月，此可視為詞話成書的上限（梅節·金瓶梅成書的上限、金瓶梅成書於萬曆的新材料）。大概公元十六世紀末葉、萬曆二十年或稍後，詞話一些不足本已在文人圈子中傳抄。當時如王宇泰、董其昌、袁宏道、王百榖、文在茲、丘志充、謝肇淛、袁中道、沈德符等均有過錄本。在輾轉傳抄過程中，開始形成兩種本子，一為原民間藝人的十卷本，書名《金瓶梅詞話》，有欣欣子序。一為經文士改編的二十卷本，書名《金瓶梅》，有東吳弄珠客序和廿公跋。十卷本《金瓶梅詞話》雖更接近藝人原本，它的刊行卻在文人改編的二十卷本《金瓶梅》之後。現存之《新鎸繡像批評金瓶梅》，是這個二十卷本的第二代刻本（梅節·新刻金瓶梅詞話後出考）。",
